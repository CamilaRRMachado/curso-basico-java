package com.camila.cursobjava.aula17;

public class For {

	public static void main(String[] args) {
		// for "normal"

		for (int i = 0; i < 5; i++) {
			System.out.println("i tem valor: " + i);
		}
		System.out.println("-----");
		for (int i = 5; i > 0; i--) {
			System.out.println("i tem valor: " + i);
		}

	}

}
